import UIKit

extension UIColor {
    static func buttonRed() -> UIColor {
        #colorLiteral(red: 0.8156862745, green: 0.007843137255, blue: 0.1058823529, alpha: 1)
    }

    static func buttonBlack() -> UIColor {
        #colorLiteral(red: 0.2, green: 0.2, blue: 0.2, alpha: 1)
    }

    static func mainWhite() -> UIColor {
        #colorLiteral(red: 0.968627451, green: 0.9725490196, blue: 0.9921568627, alpha: 1)
    }
}
