import UIKit

extension UIFont {
    static func avenir20() -> UIFont? {
        UIFont.init(name: "avenir", size: 20)
    }
    
    static func avenir26() -> UIFont? {
        UIFont.init(name: "avenir", size: 26)
    }
}
